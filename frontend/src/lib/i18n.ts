export function t(key: string, locale: Locale = "en"): string {
  return translations[locale]?.[key] ?? translations.en[key] ?? key;
}

export function getTranslator(locale: Locale) {
  return (key: string) => t(key, locale);
}

// React hook — reads current language from ThemeProvider
import { useCallback } from "react";
import { useTheme } from "@/components/theme-provider";

export function useT() {
  const { settings } = useTheme();
  const locale = (settings.language === "ar" ? "ar" : "en") as Locale;
  return useCallback((key: string) => t(key, locale), [locale]);
}

/** Returns a BCP-47 locale id for date/number formatting (e.g. "ar-EG" or "en-US") */
export function useLocaleId(): string {
  const { settings } = useTheme();
  return settings.language === "ar" ? "ar-EG" : "en-US";
}
